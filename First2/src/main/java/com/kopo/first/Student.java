package com.kopo.first;

public class Student {
	int idx;  // 고유값
	String name;
	double middleScore;
	double finalScore;
	String created;
	
	
	
	Student(){
		
	}
	
	Student(String name, double middlescore, double finalScore, String created){
		this.idx = idx;
		this.name = name;
		this.middleScore = middlescore;
		this.finalScore = middlescore;
		this.created = created;
		
	}

}


