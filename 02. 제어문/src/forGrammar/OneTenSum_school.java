package forGrammar;

public class OneTenSum_school {
	public static void main(String[] args) {
		
		int a[] = {0, 0, 0, 0};
		
		System.out.println(a[3]);
		
		
		a[0] = 10;
		a[0]++;
		
		System.out.println(a[0]);
		
		for (int i = 0; i < a.length; i++) {
			System.out.println(a[i]);
		}
		
		
	}

}
